    private void _okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event__okButtonActionPerformed
        Vector<Object> retour = new Vector<>();
        if (_choixVoitureTextField.getText().isEmpty()
                || _immatriculationTextField.getText().isEmpty()
                || (_clientsComboBox.getSelectedIndex() == -1 && ((String)_clientsComboBox.getSelectedItem()).isEmpty())
                || _typeTravailCombo.getSelectedIndex() == -1)
            JOptionPane.showMessageDialog(this,"Il y a des champs obligatoire qui sont vide","Erreur",JOptionPane.ERROR_MESSAGE);
        else
        {
            retour.add(_choixVoitureTextField.getText());
            retour.add(_immatriculationTextField.getText());
            if (_clientsComboBox.getSelectedItem() instanceof Client)
                retour.add(_clientsComboBox.getSelectedItem());
            else
            {
                String[] np = ((String)_clientsComboBox.getSelectedItem()).split(" ");
                if (np.length >= 2)
                    retour.add(new Client(np[0],np[1],null,null,null));
                else if (np.length == 1)
                    retour.add(new Client(np[0],null,null,null,null));
            }
            retour.add((_entretienRadioButton.isSelected() ? "Entretiens " : "Réparation de/du" ) + _typeTravailCombo.getSelectedItem());
            retour.add(_instructionTextArea.getText());
            ((Panel)getParent()).ajout(retour,_entretienRadioButton.isSelected());
            dispose();
        }
    }//GEN-LAST:event__okButtonActionPerformed

