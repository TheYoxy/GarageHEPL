    private void _newCheckBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event__newCheckBoxActionPerformed
        if (_newCheckBox.isSelected())
        {
            _position = _clientsComboBox.getSelectedIndex();
            _clientsComboBox.setSelectedIndex(-1);
            _clientsComboBox.setEditable(true);
        }
        else
        {
            _clientsComboBox.setSelectedIndex(_position);
            _clientsComboBox.setEditable(false);
        }
    }//GEN-LAST:event__newCheckBoxActionPerformed

